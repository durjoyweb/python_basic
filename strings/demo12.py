# lower() method in the python 

str = "YES, THIS IS A UPPER CASE"

print( str.lower() )



# lstrip() method in the python 

str = "##### YES, THIS IS A UPPER CASE"

print( str.lstrip("#") )



# replace() method in the python 

str = "YES, THIS IS A UPPER CASE YES"

print( str.replace("YES","NO",1) )


