# rsplit() method in the python 

str2 = "YES, THIS IS A UPPER CASE"

print( str2.rsplit(",",2) )



# rstrip() method in the python 

str2 = "YES, THIS IS A UPPER CASE ####"
str3 = "YES, THIS IS A UPPER CASE       "

print( str2.rstrip("#") )
print( str3.rstrip() )



# split() method in the python 

str2 = "YES$$THIS$$A$$UPPER$$CASE"

print( str2.split("$$") )